package ExercPropostosJava;

public class SomaDeDoisNumeros {
	
	public static void main(String[] args) {
        soma(10, 229);
        soma(-80, 29);
        soma(11, 15);
        soma(65, 40);
    }

	public static void soma(int x, int y) {
        System.out.println(x + " + " + y + " = " + (x + y));
    }
}

